class Difference:
    def __init__(self):
        self.difference = 0
        self.distance = 0
        

    def setDistance(self, num):
        self.distance = num
    
    def getDistance(self):
        return self.distance


    def setDifference(self,num):
        self.difference = num
        
    def getDifference(self):
        return self.difference